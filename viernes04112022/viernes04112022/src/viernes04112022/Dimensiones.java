package viernes04112022;

public class Dimensiones {
  private int anchira;
  private int altura;
  private int profundidad;
public Dimensiones(int anchira, int altura, int profundidad) {
	super();
	this.anchira = anchira;
	this.altura = altura;
	this.profundidad = profundidad;
}
public int getAnchira() {
	return anchira;
}
public void setAnchira(int anchira) {
	this.anchira = anchira;
}
public int getAltura() {
	return altura;
}
public void setAltura(int altura) {
	this.altura = altura;
}
public int getProfundidad() {
	return profundidad;
}
public void setProfundidad(int profundidad) {
	this.profundidad = profundidad;
}
  
}
